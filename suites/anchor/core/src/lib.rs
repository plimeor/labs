    pub fn search_notes(request: Value, model: &Model) -> Result<Vec<Value>, CoreError> {
        require_open_model(model).map_err(|_| CoreError::VaultNotOpen)?;
        let query = string_at(&request, "query")
            .unwrap_or("")
            .trim()
            .to_lowercase();
        let limit = request["limit"].as_u64().unwrap_or(30).max(1) as usize;
        let fields = request["fields"]
            .as_array()
            .cloned()
            .unwrap_or_else(|| vec![json!("id"), json!("title"), json!("snippet")]);

        let mut results = Vec::new();
        for note in model
            .notes
            .iter()
            .filter(|note| note_matches_filters(note, &request))
        {
            let searchable = searchable_text(note);
            if !query.is_empty() && !searchable.to_lowercase().contains(&query) {
                continue;
            }

            let mut matched_fields = Vec::new();
            if query.is_empty()
                || string_at(&note["metadata"], "title")
                    .unwrap_or("")
                    .to_lowercase()
                    .contains(&query)
            {
                matched_fields.push(json!("title"));
            }
            if query.is_empty()
                || note["body"]
                    .as_str()
                    .unwrap_or("")
                    .to_lowercase()
                    .contains(&query)
            {
                matched_fields.push(json!("body"));
            }
            if crate::domain::metadata_tags(note)
                .iter()
                .any(|tag| tag.to_lowercase().contains(&query))
            {
                matched_fields.push(json!("tag"));
            }
            if string_at(&note["metadata"], "type")
                .unwrap_or("")
                .to_lowercase()
                .contains(&query)
            {
                matched_fields.push(json!("type"));
            }

            let mut result = json!({
                "id": note_id(note),
                "kind": kind(note),
                "matchedFields": matched_fields,
                "pathHint": note["filePath"],
                "title": title(note),
                "type": note["metadata"].get("type").cloned().unwrap_or(Value::Null)
            });

            if fields.iter().any(|field| field == "snippet") {
                result["snippet"] =
                    json!(create_snippet(note["body"].as_str().unwrap_or(""), &query));
            }
            if fields.iter().any(|field| field == "metadata") {
                result["metadata"] = note["metadata"].clone();
            }
            if fields.iter().any(|field| field == "body") {
                result["body"] = note["body"].clone();
            }
            results.push(result);
        }

        Ok(results.into_iter().take(limit).collect())
    }

