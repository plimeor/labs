    pub fn create_note(input: Value, model: &mut Model, vault: &Path) -> Result<Value, CoreError> {
        let title_str = string_at(&input, "title")
            .unwrap_or("Untitled")
            .trim()
            .to_string();
        let body = string_at(&input, "body")
            .map(str::to_string)
            .unwrap_or_else(|| {
                format!(
                    "# {title_str}\n\nWrite from Markdown. Link with [[Anchor V1]] and tag with #idea.\n"
                )
            });
        let now = now_iso();
        let mut metadata = json!({
            "aliases": [],
            "created": now,
            "id": next_id("note"),
            "kind": "note",
            "properties": {},
            "tags": [],
            "title": title_str,
            "updated": now
        });
        if let Some(note_type) = string_at(&input, "type") {
            metadata["type"] = json!(note_type);
        }

        let file_path = format!(
            "notes/{}.md",
            slugify(string_at(&metadata, "title").unwrap_or("untitled"))
        );
        let note = write_note_file(vault, &unique_file_path(vault, &file_path), metadata, &body)
            .map_err(|e| CoreError::Io(e))?;
        model.notes.insert(0, note.clone());

        let operation = operation_value(json!({
            "actorId": "local-user",
            "actorType": "user",
            "approvalState": "accepted",
            "baseRevisions": {},
            "graphImpactSummary": "New note is available to relation projection after rebuild.",
            "mode": "manual",
            "operationType": "create_note",
            "provenance": "tauri operation core",
            "resultingRevisions": revision_object(note_id(&note), revision(&note)),
            "targetNoteIds": [note_id(&note)]
        }));
        append_operation(vault, model, operation).map_err(|e| CoreError::Io(e))?;
        rebuild_sqlite_projection(vault, &model.notes).map_err(|e| CoreError::Io(e))?;
        Ok(note)
    }

    pub fn update_note(input: Value, model: &mut Model, vault: &Path) -> Result<Value, CoreError> {
        let target_id = string_at(&input, "noteId")
            .ok_or_else(|| CoreError::Usage("missing_note_id".to_string()))?
            .to_string();
        let base_revision = string_at(&input, "baseRevision")
            .ok_or_else(|| CoreError::Usage("missing_base_revision".to_string()))?
            .to_string();
        let index = find_note_index(model, &target_id).map_err(|e| CoreError::NotFound(e))?;
        let before = model.notes[index].clone();

        if revision(&before) != base_revision {
            return Err(CoreError::Conflict("conflict".to_string()));
        }

        let mut metadata = before["metadata"].as_object().cloned().unwrap_or_default();
        if let Some(patch) = input.get("metadataPatch").and_then(Value::as_object) {
            apply_metadata_patch(&mut metadata, patch);
        }
        metadata.insert("updated".to_string(), json!(now_iso()));

        let body = string_at(&input, "body")
            .map(str::to_string)
            .unwrap_or_else(|| before["body"].as_str().unwrap_or_default().to_string());
        let file_path = before["filePath"]
            .as_str()
            .ok_or_else(|| CoreError::Usage("missing_file_path".to_string()))?
            .to_string();
        let updated = write_note_file(vault, &file_path, Value::Object(metadata), &body)
            .map_err(|e| CoreError::Io(e))?;
        model.notes[index] = updated.clone();

        let operation = operation_value(json!({
            "actorId": "local-user",
            "actorType": "user",
            "approvalState": "accepted",
            "baseRevisions": revision_object(&target_id, revision(&before)),
            "graphImpactSummary": graph_impact_summary(&before, &updated),
            "mode": "manual",
            "operationType": "update_note",
            "provenance": "local editor save",
            "resultingRevisions": revision_object(&target_id, revision(&updated)),
            "targetNoteIds": [target_id]
        }));
        append_operation(vault, model, operation).map_err(|e| CoreError::Io(e))?;
        rebuild_sqlite_projection(vault, &model.notes).map_err(|e| CoreError::Io(e))?;
        Ok(updated)
    }

