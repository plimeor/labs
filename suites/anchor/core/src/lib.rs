// ---------------------------------------------------------------------------
// CoreError — stable error taxonomy used by CLI exit codes + JSON envelope
// ---------------------------------------------------------------------------

#[derive(Debug, Clone)]
pub enum CoreError {
    /// Referenced note / resource does not exist.
    NotFound(String),
    /// Optimistic-concurrency revision mismatch (--if-match failed).
    Conflict(String),
    /// Operation is blocked or unsupported in the current mode.
    Blocked(String),
    /// No vault is currently open.
    VaultNotOpen,
    /// Filesystem I/O failure.
    Io(String),
    /// Bad caller input / usage error.
    Usage(String),
}

impl CoreError {
    /// Stable code string used in JSON `{"error":{"code","message"}}` envelopes
    /// and as a basis for CLI exit codes.
    pub fn code(&self) -> &'static str {
        match self {
            CoreError::NotFound(_) => "not_found",
            CoreError::Conflict(_) => "conflict",
            CoreError::Blocked(_) => "blocked",
            CoreError::VaultNotOpen => "vault_not_open",
            CoreError::Io(_) => "io",
            CoreError::Usage(_) => "usage",
        }
    }

    /// CLI exit code mapping (matches the global I/O contract in the plan doc).
    /// 0=ok · 1=usage · 2=not_found · 3=conflict · 4=blocked · 5=vault_not_open · 6=io
    pub fn exit_code(&self) -> i32 {
        match self {
            CoreError::Usage(_) => 1,
            CoreError::NotFound(_) => 2,
            CoreError::Conflict(_) => 3,
            CoreError::Blocked(_) => 4,
            CoreError::VaultNotOpen => 5,
            CoreError::Io(_) => 6,
        }
    }

    pub fn message(&self) -> &str {
        match self {
            CoreError::NotFound(msg)
            | CoreError::Conflict(msg)
            | CoreError::Blocked(msg)
            | CoreError::Io(msg)
            | CoreError::Usage(msg) => msg,
            CoreError::VaultNotOpen => "vault_not_open",
        }
    }
}

impl std::fmt::Display for CoreError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "[{}] {}", self.code(), self.message())
    }
}

impl std::error::Error for CoreError {}

/// Convert a CoreError to the legacy `String` error used by Tauri commands.
/// This preserves the exact string codes the TS frontend already tests for.
impl From<CoreError> for String {
    fn from(err: CoreError) -> String {
        err.message().to_string()
    }
}

// ---------------------------------------------------------------------------
// AnchorState — shared Tauri managed state (declared here so both main.rs and
// agent_fixtures / operations can refer to it without a circular import).
// ---------------------------------------------------------------------------

pub struct AnchorState {
    pub model: Mutex<Model>,
}

impl Default for AnchorState {
    fn default() -> Self {
        Self {
            model: Mutex::new(initial_model()),
        }
    }
}

fn initial_model() -> Model {
    let mut model = Model::default();
    let Ok(workspace_root) = vault::locate_workspace_root() else {
        return model;
    };

    let vault_path = workspace_root.join("desktop/demo/vault");
    // Guard on the committed `notes/` dir, not `.anchor/config` — the .anchor/
    // projection is git-ignored and regenerated by rebuild_vault on load, so the
    // toml is absent on a fresh checkout while the Markdown is always present.
    if vault_path.join("notes").is_dir() {
        if let Err(error) = vault::rebuild_vault(&vault_path, &mut model) {
            eprintln!("failed to restore existing demo vault: {error}");
        }
    }

    model
}

