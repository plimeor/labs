    /// List notes with default status filter: excludes archived and trashed notes.
    /// The Tauri command wrapper calls this directly — signature must not change.
    pub fn list_notes(model: &Model) -> Result<Vec<Value>, CoreError> {
        require_open_model(model).map_err(|_| CoreError::VaultNotOpen)?;
        Ok(model
            .notes
            .iter()
            .filter(|note| {
                let status =
                    crate::domain::string_at(&note["metadata"], "status").unwrap_or("active");
                status != "archived" && status != "trashed"
            })
            .map(note_to_summary)
            .collect())
    }

    /// List ALL notes including archived and trashed. Used by CLI opt-in flags.
    /// Not exposed as a Tauri command (no signature constraint).
    pub fn list_notes_all(model: &Model) -> Result<Vec<Value>, CoreError> {
        require_open_model(model).map_err(|_| CoreError::VaultNotOpen)?;
        Ok(model.notes.iter().map(note_to_summary).collect())
    }

    pub fn read_note(note_id: String, model: &Model) -> Result<Value, CoreError> {
        require_open_model(model).map_err(|_| CoreError::VaultNotOpen)?;
        find_note(model, &note_id)
            .cloned()
            .map_err(|e| CoreError::NotFound(e))
    }

    pub fn open_today_journal(model: &mut Model, vault: &Path) -> Result<Value, CoreError> {
        let today = Local::now().format("%Y-%m-%d").to_string();

        if let Some(note) = model.notes.iter().find(|note| {
            note["metadata"]["kind"] == "journal" && note["metadata"]["journalDate"] == today
        }) {
            return Ok(note.clone());
        }

        let year = &today[0..4];
        let month = &today[5..7];
        let now = now_iso();
        let body = format!(
            "# {today}\n\n- [ ] Capture today's decisions\n- [ ] Link notes with [[Anchor V1]]\n"
        );
        let metadata = json!({
            "aliases": [],
            "created": now,
            "id": format!("journal_{}", today.replace('-', "_")),
            "journalDate": today,
            "kind": "journal",
            "properties": {},
            "tags": ["journal"],
            "title": today,
            "updated": now
        });
        let file_path = format!("journals/{year}/{month}/{today}.md");
        let note =
            write_note_file(vault, &file_path, metadata, &body).map_err(|e| CoreError::Io(e))?;
        model.notes.insert(0, note.clone());

        let operation = operation_value(json!({
            "actorId": "local-user",
            "actorType": "user",
            "approvalState": "accepted",
            "baseRevisions": {},
            "graphImpactSummary": "Journal created once for local date.",
            "mode": "manual",
            "operationType": "create_journal",
            "provenance": "tauri operation core",
            "resultingRevisions": revision_object(note_id(&note), revision(&note)),
            "targetNoteIds": [note_id(&note)]
        }));
        append_operation(vault, model, operation).map_err(|e| CoreError::Io(e))?;
        rebuild_sqlite_projection(vault, &model.notes).map_err(|e| CoreError::Io(e))?;
        Ok(note)
    }

