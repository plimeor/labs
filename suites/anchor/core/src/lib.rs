    pub fn open_vault(path: String, model: &mut Model) -> Result<Value, CoreError> {
        let vault = std::path::PathBuf::from(&path);
        if vault.as_os_str().is_empty() {
            return Err(CoreError::Usage("missing_vault_path".to_string()));
        }
        ensure_vault_structure(&vault).map_err(|e| CoreError::Io(e))?;
        rebuild_vault(&vault, model).map_err(|e| CoreError::Io(e))?;
        Ok(diagnostics_value(model))
    }

